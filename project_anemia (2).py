def stratified_patient_split(df, n_splits=5, random_state=42):
    skf = StratifiedGroupKFold(n_splits=n_splits, shuffle=True, random_state=random_state)
    X = df.index
    y = df['anemia_label']
    groups = df['patient_number']
    splits = list(skf.split(X, y, groups))
    return splits

